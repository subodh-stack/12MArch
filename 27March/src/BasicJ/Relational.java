package BasicJ;

public class Relational {
	public static void main(String[] args) {
		int krupali = 91;
		int ram = 90;
		
		boolean result = krupali == ram;
		System.out.println(result);
		
		boolean r = krupali != ram;
		System.out.println(r);
		
		boolean greater = krupali > ram;
		System.out.println(greater);
		
		boolean less = krupali < ram;
		System.out.println(less);
		
		boolean greatequal = krupali >= ram;
		System.out.println(greatequal);
		
		boolean lessequal = krupali <= ram;
		System.out.println(lessequal);
		
	}
}
