package BasicJ;

public class FLoop {
	public static void main(String[] args) {
		
		
		
		for(int a=1; a<=5;a++) //1,2,3,4,5,6
		{
			for(int b =1;b<=a;b++)//1,2,3,4,5
			{
				System.out.print(" *");
			}						
			System.out.println();	
		}
	
		for(int a=1; a<=10;a++) //1,2,3,4,5,6
		{
			for(int b =1;b<=a;b++)//1,2,3,4,5
			{
				System.out.print(" *");
			}						
			System.out.println();	
		}
		for(int a=1; a<=15;a++) //1,2,3,4,5,6
		{
			for(int b =1;b<=a;b++)//1,2,3,4,5
			{
				System.out.print(" *");
			}						
			System.out.println();	
		}
		
	}
}

